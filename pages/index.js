import Head from "next/head";
import client from "../apollo";
import HOME from "../queries/homepage";
import { Container } from "../components/homepage/General";
import About from "../components/homepage/About";
import Stack from "../components/homepage/Technologies";
import Jobs from "../components/homepage/Jobs";

export default function Home({ home }) {
  return (
    <Container>
      <Head>
        <title>{`${home.About.firstName}'s website.`}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <About description={home.About.description} />
      <Stack techs={home.Body.Stack} />
      <Jobs jobList={home.Body.Job} />
    </Container>
  );
}

export async function getStaticProps(ctx) {
  const { data } = await client.query({
    query: HOME,
  });

  return {
    props: {
      home: data.homepage,
    },
    revalidate: 1,
  };
}
